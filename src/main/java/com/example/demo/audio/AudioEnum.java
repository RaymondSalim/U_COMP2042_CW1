package com.example.demo.audio;

/**
 * Enum representing different types of audio resources in the application.
 */
public enum AudioEnum {
    /**
     * Background music.
     */
    BG_MUSIC,

    /**
     * Sound for button click events.
     */
    BUTTON_CLICK,

    /**
     * Sound for checkbox toggle events.
     */
    CHECKBOX_TOGGLE,

    /**
     * Sound for player shooting action.
     */
    PLAYER_SHOOT,

    /**
     * Sound effect for star action (first star).
     */
    STAR_1,

    /**
     * Sound effect for star action (second star).
     */
    STAR_2,

    /**
     * Sound effect for star action (third star).
     */
    STAR_3
}
